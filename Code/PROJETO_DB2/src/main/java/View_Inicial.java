    private void pesquisar_produto(){
        String sql = "select prod_codigo, prod_nome, prod_descricao, prod_marca, prod_modelo, prod_preco, prod_categoria from tbproduto where prod_nome like ?";
        try {
            pst = conexao.prepareStatement(sql);
            //passando o conteúdo da caixa de pesquisa para o ?
            //atenção ao % - continuação da String sql
            pst.setString(1, ctPesquisa.getText() + "%");
            rs = pst.executeQuery();
            //a linha abaixo usa a biblioteca rs2xml.jar para preencher a tabela
            TabProd.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
