    public void ShowTable() {
        String sql = "select prod_codigo, prod_nome, prod_descricao, prod_marca, prod_modelo, prod_preco, prod_categoria from tbproduto";
        try {
            pst = conexao.prepareStatement(sql);
            rs = pst.executeQuery();
            ResultSetMetaData rsmd = rs.getMetaData();
            DefaultTableModel model = (DefaultTableModel) TabProd.getModel();
            model.setRowCount(0);

            String codigo, nome, descricao, marca, modelo, preco, categoria;
            while (rs.next()) {
                codigo = rs.getString(1);
                nome = rs.getString(2);
                descricao = rs.getString(3);
                marca = rs.getString(4);
                modelo = rs.getString(5);
                preco = rs.getString(6);
                categoria = rs.getString(7);
                String[] row = {codigo, nome, descricao, marca, modelo, preco, categoria};
                model.addRow(row);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
